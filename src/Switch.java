public class Switch {
    public static void main(String[] args) {
        int i = 10;
       switch (i) {
          
           case 5:
               System.out.println("Five");
               //break;
           case 10:
               System.out.println("TEN");
               //break;
           case 15:
               System.out.println("FIFTEEN");
               //break;
           case 20:
               System.out.println("TWENTY");
               break;
           default:
               System.out.println("Default Case");
               //break;
       }

    }
    
}
