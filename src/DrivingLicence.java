

public class DrivingLicence {
    private String licenceNum;
    private String licenceTpye;
    private String licenceName;
    private String licenceDate;
    private String licenceExpDate;

    public void setLicenceNum(String licenceNum){
        this.licenceNum = licenceNum;
    }
    public String getLicenceNum(){
        return licenceNum;
    }
    public void setLicenceTpye(String licenceTpye){
        this.licenceTpye = licenceTpye;
    }
    public String getLicenceType(){
        return licenceTpye;
    }
    public void setLicenceName(String licenceName){
        this.licenceName = licenceName;
    }
    public String getLicenceName(){
        return licenceName;
    }
    public void setLicenceDate(String licenceDate){
        this.licenceDate = licenceDate;
    }
    public String getLicenceDate(){
        return licenceDate;
    }
    public void setLicenceExpDate(String licenceExpDate){
        this.licenceExpDate = licenceExpDate;
    }
    public String getLicenceExpDate(){
        return licenceExpDate;
    }
    
}
